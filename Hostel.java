public class Hostel {
  private String name;
  private String location;
  private int age;

  public void setName(String residentName) {
      name = residentName;
  }
  public String getName(){
      return name;
  }
  public void setLocation(String residentLocation) {
      location = residentLocation;
  }
  public String getLocation() {
      return location;
  }
  public void setAge(int residentAge) {
      age = residentAge;
  }
  public int getAge() {
      return age;
  }
}
